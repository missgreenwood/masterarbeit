#!/usr/local/bin/Rscript

percent_time = function(list, bftree) {bftree/(list/100)}
lablist <- as.vector(0:9)
t <- "CPU-Zeit für k-nächste-Nachbarn-Suche"
s256 <- "n=3, Filtergröße 256"
sub256 <- list(s256, col="black", cex=0.9)
s512 <- "n=3, Filtergröße 512"
sub512 <- list(s512, col="black", cex=0.9)

cputimenn3_256 <- read.csv("/Users/judith/Documents/MA/src/src/cputime_nn3_256.csv", sep=",")
attach(cputimenn3_256)
pdf('/Users/judith/Documents/MA/evaluation/cputime_nn3_256.pdf')
plot(QF3NN256, CPUTimeUList, col="darkolivegreen4", xlab="Query-Filter", ylab="CPU-Zeit in µs", xlim=c(0, 10), ylim=c(0, 5400), type="p", pch=16, xaxt="n", yaxs="i")
lines(QF3NN256, CPUTimeBFTree, col="dodgerblue4", type="p", pch=16)
axis(1, at=0:9, labels=lablist)
text(9.37, 3400, "Unsortierte Liste", cex=0.75)
text(9.4, 1000, "BloomFilterTree", cex=0.75)
title(main=t, line=3)
title(sub256)
l256 <- c(CPUTimeUList)
b256 <- c(CPUTimeBFTree)
pdf('/Users/judith/Documents/MA/evaluation/percent_time_nn3_256.pdf')
plot(percent_time(l256, b256), col="darkcyan", xlab="Query-Filter", ylab="CPU-Zeit in Prozent gegenüber unsortierter Liste", xlim=c(0, 10), ylim=c(0,100), type="p", pch=16, xaxt="n", yaxs="i")
axis(1, at=1:10, labels=lablist)
title(main=t, line=3)
title(sub256)
detach(cputimenn3_256)

cputimenn3_512 <- read.csv("/Users/judith/Documents/MA/src/src/cputime_nn3_512.csv", sep=",")
attach(cputimenn3_512)
pdf('/Users/judith/Documents/MA/evaluation/cputime_nn3_512.pdf')
plot(QF3NN512, CPUTimeUList, col="darkolivegreen", xlab="Query-Filter", ylab="CPU-Zeit in µs", xlim=c(0, 10), ylim=c(0, 7100), type="p", pch=16, xaxt="n", yaxs="i")
lines(QF3NN512, CPUTimeBFTree, col="dodgerblue4", type="p", pch=16)
axis(1, at=0:9, labels=lablist)
text(9.37, 6400, "Unsortierte Liste", cex=0.75)
text(9.4, 1600, "BloomFilterTree", cex=0.75)
title(main=t, line=3)
title(sub512)
l512 <- c(CPUTimeUList)
b512 <- c(CPUTimeBFTree)
pdf('/Users/judith/Documents/MA/evaluation/percent_time_nn3_512.pdf')
plot(percent_time(l512, b512), col="darkcyan", xlab="Query-Filter", ylab="CPU-Zeit in Prozent gegenüber unsortierter Liste", xlim=c(0, 10), ylim=c(0,100), type="p", pch=16, xaxt="n", yaxs="i")
axis(1, at=1:10, labels=lablist)
title(main=t, line=3)
title(sub512)
detach(cputimenn3_512)
