percent_dev = function(sollwert, messwert) {((abs(sollwert-messwert))/messwert)*100}
lablist <- as.vector(0:9)
t <- "k-nächste-Nachbarn-Suche"
s256 <- "n=1, Filtergröße 256"
sub256 <- list(s256, col="black", cex=0.9)
s512 <- "n=1, Filtergröße 512"
sub512 <- list(s512, col="black", cex=0.9)
qualitynn_256 <- read.csv("/Users/judith/Documents/MA/src/src/nn_256.csv", sep=",")
attach(qualitynn_256)
pdf('Documents/MA/evaluation/nn_256.pdf')
plot(QFNN256, MaxNN256, col="red", xlab="Query-Filter", ylab="Jaccard-Distanzen zum nächsten Nachbarn", xlim=c(0, 10), ylim=c(0.0, 0.8), type="p", pch=16, xaxt="n")
lines(QFNN256, OptimalNN256, col="darkolivegreen4", type="p", pch=16)
lines(QFNN256, ComputedNN256, col="dodgerblue4", type="p", pch=16)
axis(1, at=0:9, labels=lablist)
text(9.5, 0.75, "Maximaler Wert", cex=0.75)
text(9.4, 0.62, "Berechneter Wert", cex=0.75)
text(9.5, 0.5, "Optimaler Wert", cex=0.75)
title(main=t, line=3)
title(sub256)
dev.off()
s256 <- c(OptimalNN256)
m256 <- c(ComputedNN256)
pdf('Documents/MA/evaluation/percent_dev_nn_256.pdf')
plot(percent_dev(s256, m256), col="darkcyan", xlab="Query-Filter", ylab="Prozentuale Abweichung vom optimalen Wert", xlim=c(1, 10), ylim=c(0, 100), type="l", pch=16, xaxt="n", yaxs="i")
axis(1, at=1:10, labels=lablist)
title(main=t, line=3)
title(sub256)
dev.off()
