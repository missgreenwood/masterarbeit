#!/usr/local/bin/Rscript

se = function(sollwert, messwert) {(messwert-sollwert)^2}
lablist <- as.vector(0:9)

qualitynn_256 <- read.csv("/Users/judith/Documents/MA/src/src/nn_256.csv", sep=",")
attach(qualitynn_256)
s256 <- c(OptimalNN256)
m256 <- c(ComputedNN256)
pdf('/Users/judith/Documents/MA/evaluation/nn_256.pdf')
par(mar=c(5.1,5.1,4.1,2.1))
plot(QFNN256, OptimalNN256, col="darkolivegreen3", type="p", pch=16, xlim=c(0, 9), ylim=c(0.0, 1.0), xaxt="n", xlab="Query-Filter", ylab="Jaccard-Distanzen zum nächsten Nachbarn", cex=1.5, cex.lab=1.5, cex.axis=1.5)
par(new=TRUE)
plot(QFNN256, ComputedNN256, col="dodgerblue3", type="p", pch=16, xlim=c(0, 9), ylim=c(0.0, 1.0), xaxt="n", xlab="", ylab="", cex=1.5, cex.lab=1.5, cex.axis=1.5)
par(new=TRUE)
plot(se(s256, m256), type="p", col="red", pch=16, xlim=c(1,9), ylim=c(0.0, 1.0), xaxt="n", xlab="", ylab="", cex=1.5, cex.lab=1.5, cex.axis=1.5)
axis(1, at=0:9, labels=lablist, cex.lab=1.5, cex.axis=1.5)
legend(4.5,1.0, c("k=1, Filtergröße 256", "Messwert", "Sollwert", "Quadratischer Fehler"), cex=1.5, pch=16, col=c("white","darkolivegreen3","dodgerblue3", "red")) 
detach(qualitynn_256)

