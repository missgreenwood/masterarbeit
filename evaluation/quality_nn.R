#!/usr/local/bin/Rscript

se = function(sollwert, messwert) {(messwert-sollwert)^2}
lablist <- as.vector(0:9)
t <- "k-nächste-Nachbarn-Suche"
s256 <- "k=1, Filtergröße 256"
sub256 <- list(s256, col="black", cex=0.9)
s512 <- "k=1, Filtergröße 512"
sub512 <- list(s512, col="black", cex=0.9)

qualitynn_256 <- read.csv("/Users/judith/Documents/MA/src/src/nn_256.csv", sep=",")
attach(qualitynn_256)
s256 <- c(OptimalNN256)
m256 <- c(ComputedNN256)
pdf('/Users/judith/Documents/MA/evaluation/nn_256.pdf')
plot(QFNN256, OptimalNN256, col="darkolivegreen3", type="p", pch=16, xlim=c(0, 9), ylim=c(0.0, 0.8), xaxt="n", xlab="Query-Filter", ylab="Jaccard-Distanzen zum nächsten Nachbarn")
par(new=TRUE)
plot(QFNN256, ComputedNN256, col="dodgerblue3", type="p", pch=16, xlim=c(0, 9), ylim=c(0.0, 0.8), xaxt="n", xlab="", ylab="")
par(new=TRUE)
plot(se(s256, m256), type="p", col="red", pch=16, xlim=c(1,10), ylim=c(0.0, 0.8), xaxt="n", xlab="", ylab="")
axis(1, at=1:10, labels=lablist)
text(9.8, 0.66, "Messwert", cex=0.75)
text(9.8, 0.49, "Sollwert", cex=0.75)
text(9.2, 0.08, "Quadratischer Fehler", cex=0.75)
title(main=t, line=3)
title(sub256)
detach(qualitynn_256)

qualitynn_512 <- read.csv("/Users/judith/Documents/MA/src/src/nn_512.csv", sep=",")
