percent_dev = function(sollwert, messwert) {((abs(sollwert-messwert))/messwert)*100}
lablist <- as.vector(0:9)
t <- "k-nächste-Nachbarn-Suche"
s256 <- "n=3, Filtergröße 256"
sub256 <- list(s256, col="black", cex=0.9)
s512 <- "n=3, Filtergröße 512"
sub512 <- list(s512, col="black", cex=0.9)
qualitynn3_256 <- read.csv("/Users/judith/Documents/MA/src/src/nn3_256.csv", sep=",")
attach(qualitynn3_256)
pdf('Documents/MA/evaluation/nn3_256-1.pdf')
plot(QF3NN256, Max3NN256, col="red", xlab="Query-Filter", ylab="Jaccard-Distanzen der 3 nächsten Nachbarn im Baum", xlim=c(0, 10), ylim=c(0.0, 0.8), type="p", pch=16, xaxt="n")
axis(1, at=0:9, labels=lablist)
lines(QF3NN256, Optimal1NN256, col="darkolivegreen4", type="p")
lines(QF3NN256, Optimal2NN256, col="darkolivegreen3", type="p")
lines(QF3NN256, Optimal3NN256, col="darkolivegreen2", type="p")
text(9.5, 0.75, "Maximal value", cex=0.75)
text(9.5, 0.62, "Computed value", cex=0.75)
text(9.5, 0.5, "Optimal value", cex=0.75)
title(main=t, line=3)
title(sub256)
dev.off()
s1 <- c(Optimal1NN256)
m1 <- c(Computed1NN256)
s2 <- c(Optimal2NN256)
m2 <- c(Computed2NN256)
s3 <- c(Optimal3NN256)
m3 <- c(Computed3NN256)
pdf('Documents/MA/evaluation/percent_dev_nn3_256.pdf')
plot((percent_dev(s1, m1) + percent_dev(s2, m2) + percent_dev(s3, m3))/3, col="darkcyan", xlab="Query filter", ylab="Mean percent deviation from optimal result", xlim=c(1, 10), ylim=c(0, 100), type="l", pch=16, xaxt="n", yaxs="i")
axis(1, at=1:10, labels=lablist)
title(main=t, line=3)
title(sub256)
dev.off()
pdf('Documents/MA/evaluation/nn3_256-2.pdf')
plot(QF3NN256, Max3NN256, col="red", xlab="Query filter", ylab="Jaccard distances of 3NN in tree", xlim=c(0, 10), ylim=c(0.0, 0.8), type="p", pch=16, xaxt="n")
axis(1, at=0:9, labels=lablist)
lines(QF3NN256, Computed1NN256, col="dodgerblue4", type="p")
lines(QF3NN256, Computed2NN256, col="dodgerblue3", type="p")
lines(QF3NN256, Computed3NN256, col="dodgerblue2", type="p")
text(9.5, 0.75, "Maximal value", cex=0.75)
text(9.5, 0.62, "Computed value", cex=0.75)
