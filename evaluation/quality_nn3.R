#!/usr/local/bin/Rscript

percent_dev = function(sollwert, messwert) {((abs(sollwert-messwert))/messwert)*100}
se = function(sollwert, messwert) {(messwert-sollwert)^2}
lablist <- as.vector(0:9)

qualitynn3_256 <- read.csv("/Users/judith/Documents/MA/src/src/nn3_256.csv", sep=",")
attach(qualitynn3_256)
s1 <- c(Optimal1NN256)
m1 <- c(Computed1NN256)
s2 <- c(Optimal2NN256)
m2 <- c(Computed2NN256)
s3 <- c(Optimal3NN256)
m3 <- c(Computed3NN256)

pdf('/Users/judith/Documents/MA/evaluation/nn3_256-2.pdf')
par(mar=c(5.1,5.1,4.1,2.1))
plot(QF3NN256, Computed1NN256, col="dodgerblue4", type="p", pch=16, xlim=c(0, 9), ylim=c(0.0, 1.0),  xaxt="n", xlab="Query-Filter", ylab="Jaccard-Distanzen zu den 3 nächsten Nachbarn", cex=1.5, cex.lab=1.5, cex.axis=1.5)
par(new=TRUE)
plot(QF3NN256, Computed2NN256, col="dodgerblue3", type="p", pch=16, xlim=c(0, 9), ylim=c(0.0, 1.0), xaxt="n", xlab="", ylab="", cex=1.5, cex.lab=1.5, cex.axis=1.5)
