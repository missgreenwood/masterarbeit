plot(QF3NN256, Computed3NN256, col="dodgerblue2", type="p", pch=16, xlim=c(0, 9), ylim=c(0.0, 1.0), xaxt="n", xlab="", ylab="", cex=1.5, cex.lab=1.5, cex.axis=1.5)
par(new=TRUE)
plot(((se(s1, m1) + se(s2, m2) + se(s3, m3))/3), type="p", col="red", pch=16, xlim=c(1,9), ylim=c(0.0, 1.0), xaxt="n", xlab="", ylab="", cex=1.5, cex.lab=1.5, cex.axis=1.5)
axis(1, at=0:9, labels=lablist, cex.lab=1.5, cex.axis=1.5)
legend(4.5,1.0, c("k=3, Filtergröße 256", "1. Nachbar", "2. Nachbar", "3. Nachbar", "Quadratischer Fehler"), cex=1.5, pch=16, col=c("white","dodgerblue4","dodgerblue3","dodgerblue2","red")) 
